import { mkdir, readFile, writeFile } from "node:fs/promises";

const dataPath = new URL("../data/sponsors.enriched.json", import.meta.url);
const outPath = new URL("../supabase/seed.sql", import.meta.url);

function sqlString(value) {
  if (value === null || value === undefined || value === "") return "null";
  return `'${String(value).replace(/'/g, "''")}'`;
}

function sqlTextArray(values) {
  const clean = [...new Set((values || []).filter(Boolean).map((value) => String(value).trim()).filter(Boolean))];
  return `array[${clean.map(sqlString).join(", ")}]::text[]`;
}

function slugify(value) {
  return String(value)
    .toLowerCase()
    .replace(/&/g, " and ")
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-|-$/g, "");
}

const data = JSON.parse(await readFile(dataPath, "utf8"));
const rows = data.sponsors.map((sponsor) => {
  const slug = slugify(sponsor.name);
  return `(
    ${sqlString(slug)},
    ${sqlString(sponsor.name)},
    ${sqlString(sponsor.website_url)},
    ${sqlString(sponsor.lookup?.final_url || sponsor.website_url)},
    ${sqlString(sponsor.phone)},
    ${sqlString(sponsor.category)},
    ${sqlString(sponsor.description)},
    ${sqlTextArray(sponsor.services)},
    ${sqlTextArray(sponsor.service_areas)},
    ${sqlTextArray(sponsor.keywords)},
    ${sqlTextArray(sponsor.source_notes)},
    ${sqlString(sponsor.source_url)},
    ${sqlString(sponsor.source_published_at)},
    ${sqlString(sponsor.lookup?.lookup_status || "not_checked")},
    ${sqlString(sponsor.lookup?.site_title || "")},
    ${sqlString(sponsor.lookup?.site_description || "")}
  )`;
});

const sql = `-- Generated by scripts/generate-sponsor-seed-sql.mjs.
-- Source: ${data.source_url}
-- Sponsors: ${data.sponsor_count}

insert into public.sponsors (
  slug,
  name,
  website_url,
  lookup_url,
  phone,
  category,
  description,
  services,
  service_areas,
  keywords,
  source_notes,
  source_url,
  source_published_at,
  lookup_status,
  site_title,
  site_description
) values
${rows.join(",\n")}
on conflict (slug) do update set
  name = excluded.name,
  website_url = excluded.website_url,
  lookup_url = excluded.lookup_url,
  phone = excluded.phone,
  category = excluded.category,
  description = excluded.description,
  services = excluded.services,
  service_areas = excluded.service_areas,
  keywords = excluded.keywords,
  source_notes = excluded.source_notes,
  source_url = excluded.source_url,
  source_published_at = excluded.source_published_at,
  lookup_status = excluded.lookup_status,
  site_title = excluded.site_title,
  site_description = excluded.site_description,
  imported_at = now(),
  updated_at = now();
`;

await mkdir(new URL("../supabase/", import.meta.url), { recursive: true });
await writeFile(outPath, sql);
console.log(`Wrote seed SQL to ${outPath.pathname}`);
